/*
                        LAÇO DE REPETIÇÃO FOR
                        
Usamos o laço for quando sabemos exatamente quantas vezes queremos repetir um bloco de instruções.
*/

//Declarando Array que será exibindo no laço for como exemplo.
var arr = ['José', 'João', 'Lucas', 'Fernanda', 'Jayane', 'Thais'];


// inicializando com a variavel (i) com o valor 0 será incrementando ate a nossa condição i < 6 se tornar verdadeira.
for (let i = 0; i < 6; i++) {
    // Laço vai exibir os elementes do array atraves do laço.
    console.log(arr[i]);
    
}