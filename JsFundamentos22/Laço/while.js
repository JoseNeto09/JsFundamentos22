/* 
                                LAÇO DE REPETIÇÃO WHILE

While – Esta instrução é usada quando não sabemos quantas vezes um determinado bloco de instruções
precisa ser repetido. Com ele, a execução das instruções vai continuar até que uma condição seja verdadeira.
A condição a ser analisada para a execução do laço de repetição deverá retornar um valor booleano

*/

// variavel de inicialização.
var i = 0;


while ( i <= 10 ) {
    
    console.log(i);
    i++;
}